function renderPortfolio() {
    const container = document.getElementById('portfolio-grid');
    container.innerHTML = ''; // Clear container

    // Group projects by category
    const grouped = PROJECTS.reduce((acc, project) => {
        if (!acc[project.category]) acc[project.category] = [];
        acc[project.category].push(project);
        return acc;
    }, {});

    const categories = ["Textile Systems", "Product & Lifestyle Prints", "Home & Interior"];

    categories.forEach((catName) => {
        const projects = grouped[catName];
        if (!projects) return;

        // Create Category Header
        const header = document.createElement('div');
        header.className = 'col-span-full mt-20 mb-10';
        header.innerHTML = `
            <h2 class="text-4xl magazine-title border-l-4 border-accent pl-6">${catName}</h2>
        `;
        container.appendChild(header);

        // Create Projects
        projects.forEach((project, i) => {
            const card = document.createElement('div');
            card.className = 'group relative aspect-[4/5] overflow-hidden rounded-2xl bg-white/5 cursor-pointer';
            card.setAttribute('data-animate', '');

            card.innerHTML = `
                <img
                    src="${project.images[0]}"
                    alt="${project.title}"
                    class="w-full h-full object-cover transition-transform duration-700 group-hover:scale-110 opacity-60 group-hover:opacity-100"
                />
                <div class="absolute top-4 right-4 bg-black/50 backdrop-blur-md px-3 py-1 rounded-full text-xs font-mono border border-white/10 z-10 text-white">
                    ${project.images.length} Photos
                </div>
                <div class="absolute inset-0 bg-gradient-to-t from-black via-transparent to-transparent opacity-80"></div>
                <div class="absolute bottom-0 left-0 p-8 w-full translate-y-4 group-hover:translate-y-0 transition-transform">
                    <span class="text-xs uppercase tracking-widest mb-2 block" style="color: var(--color-accent)">${project.category}</span>
                    <h3 class="text-2xl font-bold">${project.title}</h3>
                </div>
            `;

            card.onclick = () => openGallery(project);
            container.appendChild(card);
        });
    });

    // Refresh animations for new elements
    initAnimations();
}

// Gallery Logic
let currentProject = null;
let currentImageIndex = 0;

function openGallery(project) {
    currentProject = project;
    currentImageIndex = 0;
    updateGalleryImage();
    document.getElementById('modal-gallery').classList.add('active');
    document.body.style.overflow = 'hidden';
}

function closeGallery() {
    document.getElementById('modal-gallery').classList.remove('active');
    document.body.style.overflow = 'auto';
    currentProject = null;
}

function nextImage(e) {
    if (e) e.stopPropagation();
    if (!currentProject) return;
    currentImageIndex = (currentImageIndex + 1) % currentProject.images.length;
    updateGalleryImage();
}

function prevImage(e) {
    if (e) e.stopPropagation();
    if (!currentProject) return;
    currentImageIndex = (currentImageIndex - 1 + currentProject.images.length) % currentProject.images.length;
    updateGalleryImage();
}

function updateGalleryImage() {
    const imgElement = document.getElementById('modal-img');
    const titleElement = document.getElementById('modal-title');
    const counterElement = document.getElementById('modal-counter');

    imgElement.src = currentProject.images[currentImageIndex];
    titleElement.textContent = currentProject.title;
    counterElement.textContent = `Photo ${currentImageIndex + 1} of ${currentProject.images.length}`;
}

function initGallery() {
    document.getElementById('modal-close').onclick = closeGallery;
    document.getElementById('modal-prev').onclick = prevImage;
    document.getElementById('modal-next').onclick = nextImage;
    document.getElementById('modal-gallery').onclick = closeGallery;
}

// Animation Logic
function initAnimations() {
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
            }
        });
    }, { threshold: 0.1 });

    document.querySelectorAll('[data-animate]').forEach(el => observer.observe(el));

    // Parallax Hero
    window.addEventListener('scroll', () => {
        const scroll = window.pageYOffset;
        const heroContent = document.querySelector('.hero-parallax');
        if (heroContent) {
            heroContent.style.transform = `translateY(${scroll * 0.4}px)`;
            heroContent.style.opacity = 1 - (scroll / 600);
        }
    });
}
