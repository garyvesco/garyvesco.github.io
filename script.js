// === TYPING ANIMATION ===
function initTypingAnimation() {
    const el = document.getElementById('typing-quote');
    if (!el) return;

    const fullText = el.dataset.quote || '';
    const cursor = el.querySelector('.typing-cursor');

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting && !el.dataset.typed) {
                el.dataset.typed = 'true';
                observer.unobserve(el);
                typeText(el, cursor, fullText);
            }
        });
    }, { threshold: 0.5 });

    observer.observe(el);
}

function typeText(container, cursor, text) {
    let index = 0;
    const speed = 40; // ms per character
    const textNode = document.createTextNode('');
    container.insertBefore(textNode, cursor);

    function type() {
        if (index < text.length) {
            textNode.textContent += text.charAt(index);
            index++;
            setTimeout(type, speed);
        } else {
            // Typing done — keep cursor blinking for 1s then hide
            setTimeout(() => {
                if (cursor) cursor.classList.add('done');
            }, 1000);
        }
    }

    type();
}
